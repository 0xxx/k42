/*****************************************************************************
 * Module Description: In the machine specific files define read/write
 * privaladges for user and supervisor mode.  Note, we decided here to
 * use the least common demoninator, of the machines we know, since
 * the other modes can be generated by providing additional mappings
 * for the memory.
 * **************************************************************************/

#include <misc/hardware.H>

class AccessMode {
public:
    typedef uval pageFaultInfo;		// (opaque) machine fault info


#include __MINC(Access.H)

    /*
     * execute is an independent bit in any machines mapping
     */
    static uval isExecute(mode accessmode) {
	return (uval)accessmode & (uval)execute;
    }
    static mode RWPerms(mode perms) {
	return mode(perms & readUserReadSup);
    }
};

